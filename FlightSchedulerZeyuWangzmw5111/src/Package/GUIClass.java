    private void addFConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFConfirmActionPerformed
        String flightName;
        int seats;
        flightName = addFText.getText();
        seats = (Integer)flightSeatsSpinner.getValue();
        ArrayList<String> myFlights = Flight.getMyFlights();
        if(myFlights.contains(flightName))
        {
            addTextArea.setText("No duplicated flights allowed. Flight was already added.");
        }
        else
        {
            addFlight.addFlight(flightName, seats);
            addFText.setText("");
            addTextArea.setText("Your Flight: " + flightName + " Is Added To The Database.");
            flightCombo.setModel(new javax.swing.DefaultComboBoxModel(Flight.getMyFlights().toArray()));
        }
        this.setEnabled(true);
        addFlightDialog.dispose();
    }//GEN-LAST:event_addFConfirmActionPerformed

