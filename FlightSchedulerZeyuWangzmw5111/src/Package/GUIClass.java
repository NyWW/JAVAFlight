    private void addDateConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDateConfirmActionPerformed
        String date = (String)addYear.getSelectedItem() + "-" + (String)addMonth.getSelectedItem() +"-"+ (String)addDay.getSelectedItem();
        Date theDate = java.sql.Date.valueOf(date);
        ArrayList<Date> myDates = getMyDate.getMyDate();
        if(myDates.contains(theDate))
        {
            addTextArea.setText("No duplicated dates allowed. Date was already added.");
        }
        else
        {
            addDate.addDate(theDate);
            addTextArea.setText("Your Date: " + date + " Is Added To The Database.");
            dateCombo.setModel(new javax.swing.DefaultComboBoxModel(getMyDate.getMyDate().toArray()));
        }
        this.setEnabled(true);
        addDateDialog.dispose();
    }//GEN-LAST:event_addDateConfirmActionPerformed

